/**
 * Get all claims for a specific asset
 */
export function getClaimsForAsset(assetId) {
  return mockBusinessClaims.filter(claim => claim.assetId === assetId);
}

/**
 * Get all payments for a specific asset
 */
export function getPaymentsForAsset(assetId) {
  return mockBusinessPayments.filter(payment => payment.appliedTo === assetId);
}

/**
 * Get property by ID
 */
export function getPropertyById(propertyId) {
  return mockProperties.find(prop => prop.id === propertyId);
}

/**
 * Get vehicle by ID
 */
export function getVehicleById(vehicleId) {
  return mockVehicles.find(vehicle => vehicle.id === vehicleId);
}

/**
 * Get asset by ID (property or vehicle)
 */
export function getAssetById(assetId) {
  return getPropertyById(assetId) || getVehicleById(assetId);
}

/**
 * Calculate total monthly premium for all properties
 */
export function getTotalPropertyPremium() {
  return mockProperties.reduce((total, prop) => total + prop.monthlyPremium, 0);
}

/**
 * Calculate total monthly premium for all vehicles
 */
export function getTotalFleetPremium() {
  return mockVehicles.reduce((total, vehicle) => total + vehicle.monthlyPremium, 0);
}

/**
 * Calculate total monthly premium for all business assets
 */
export function getTotalBusinessPremium() {
  return getTotalPropertyPremium() + getTotalFleetPremium();
}

/**
 * Get count of open claims
 */
export function getOpenClaimsCount() {
  return mockBusinessClaims.filter(claim => 
    claim.status === 'In Review' || claim.status === 'Approved'
  ).length;
}

/**
 * Get properties with active status
 */
export function getActiveProperties() {
  return mockProperties.filter(prop => prop.status === 'Active');
}

/**
 * Get vehicles with active status
 */
export function getActiveVehicles() {
  return mockVehicles.filter(vehicle => vehicle.status === 'Active');
}
